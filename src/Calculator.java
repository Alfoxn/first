import java.util.Scanner;

class Calculator {

    Calculator() {
    }

    void sum(int num1, int num2) {
        System.out.println(num1 + num2);
    }

    void diff(int num1, int num2) {
        System.out.println(num1 - num2);
    }

    void mul(int num1, int num2) {
        System.out.println(num1 * num2);
    }

    void div(int num1, int num2) {
        System.out.println(num1 / num2);
    }
}